package MyExemples;

public class MyPrinter {

	public static void printString(String printable){
		System.out.println(printable+"2");
		
	}
	
	public static void printObject(Object printable){
		System.out.println(printable.toString());
	}
	public static void printInt(int printable){
		System.out.println("*_*  ____(  "+printable+" )_____  *_*");
	}
	public static void printDouble(double printable){
		System.out.println(" double_(  "+printable+" )");
	}
}
